package loops;

public class Whileloop2 {

	public static void main(String[] args) {
        int i=11;
        //Executing while with the false condition. it does not print anything
        while(i<10) {
        	System.out.println(i);
        	i++;
        }
	
	// executing do while with false condition. it executes the loop atleast once.
        
        do {
        	System.out.println("Hello Sahana");
        	
        }while(i<10);
	
	
	}

}
