package loops;

public class DoWhile{

	public static void main(String[] args) {
       int i=0;
		do {
    	   System.out.println(i);
    	   i++;
       }while(i<=10);
	
	}
	}
 
//in do while the loop is executed first and then the condition is checked. So, even if the condition is not true the loop is executed atleast once.

