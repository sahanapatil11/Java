import java.util.Scanner;
public class Pattern3 {

	public static void main(String[] args) {
          Scanner sc =  new Scanner(System.in);
          System.out.println("Enter the size");
          int size = sc.nextInt();
          for(int i=0; i<=size; i++ ) {
        	  System.out.print("#" + " ");
          }
	}

}
